package ExercicioOito;

public class AlgoritValorSeis {
    public static void main(String[] args) {

        int numA = 3, numB = 6, numC = 4, numD = 2, numE = 9, numF = 5;

        if(numA == numB){
            System.out.println("A é igual a B");

        }else {
            System.out.println("A não é igual B");
        }

        if (numC != numD){
            System.out.println("C é diferente de D");

        }else {
            System.out.println("C não é diferente de D");
        }

        if (numE > numF){
            System.out.println("E é maior que F");

        }else {
            System.out.println("E não é maior que F");
        }

    }
}
